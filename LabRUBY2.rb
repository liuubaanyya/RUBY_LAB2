# Задаємо координати вершин у вигляді масиву точок (x, y)
vertices = [
  [64, 304], [55, 281], [53, 257], [71, 217], [74, 181],
  [45, 155], [43, 128], [53, 108], [70, 89], [95, 77],
  [108, 56], [133, 50], [190, 55], [253, 71], [287, 54],
  [326, 50], [381, 56], [413, 65], [430, 115], [428, 158],
  [412, 192], [364, 222], [318, 243], [261, 253], [224, 268],
  [182, 278], [134, 290], [105, 294]
]

# Додаємо першу вершину в кінець масиву, щоб мати замкнутий багатокутник
vertices << vertices[0]

# Функція для обчислення площі багатокутника за формулою Гауса
def polygon_area(vertices)
  n = vertices.length
  area = 0.0

  for i in 0...n-1
    x1, y1 = vertices[i]
    x2, y2 = vertices[i+1]
    area += (x1 * y2 - x2 * y1)
  end

  area /= 2.0
  return area.abs
end

# Обчислимо площу багатокутника
area = polygon_area(vertices)

puts "Площа багатокутника: #{area}"
